/** Look up team-supplied SEO metadata for a pathname (no trailing slash, "/" for home). */
export function getSeo(path: string): SeoEntry | undefined {
  return seoMeta[path];
}

// Full Metadata for a fixed route whose title/description come from the sheet.
// Titles are applied as `absolute` so the site-wide title template isn't appended.
// The canonical is always the page's own path.
export function seoMetadata(path: string): Metadata {
  const s = seoMeta[path];
  return {
    ...(s ? { title: { absolute: s.title }, description: s.description } : {}),
    alternates: { canonical: path },
  };
}

// For dynamic routes: sheet metadata if present, else fall back to the page's
// own computed title/description (which still get the site-wide title template).
export function dynamicSeoMetadata(
  path: string,
  fallback: { title: string; description: string }
): Metadata {
  const s = seoMeta[path];
  return {
    title: s ? { absolute: s.title } : fallback.title,
    description: s ? s.description : fallback.description,
    alternates: { canonical: path },
  };
}
